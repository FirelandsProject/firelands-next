def map_skill_row(fields: list[str]) -> str | None:
    if len(fields) < 4:
        raise ValueError("playercreateinfo_skills row too short")
    race_mask, class_mask, skill_id, rank = fields[0], fields[1], fields[2], fields[3]
    if int(skill_id) in _EXCLUDED_STARTER_SKILL_IDS:
        return None
    return f"{race_mask},{class_mask},{skill_id},{rank}"


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--ref",
        type=Path,
        default=_REPO_ROOT / "firelands-cata-ref" / "data" / "sql" / "base" / "db_world",
    )
    parser.add_argument(
        "--tables-out",
        type=Path,
        default=_REPO_ROOT / "sql" / "migrations" / "42_world_playercreateinfo_tables.sql",
    )
    parser.add_argument(
        "--data-out",
        type=Path,
        default=_REPO_ROOT / "sql" / "migrations" / "43_world_playercreateinfo_data.sql",
    )
    args = parser.parse_args()

    ref = args.ref
    spawn_rows = [
        map_playercreateinfo_row(r)
        for r in extract_insert_rows(ref / "playercreateinfo.sql", "playercreateinfo")
    ]
    spell_rows = []
    for r in extract_insert_rows(
        ref / "playercreateinfo_spell_custom.sql", "playercreateinfo_spell_custom"
    ):
        mapped = map_spell_row(r)
        if mapped is not None:
            spell_rows.append(mapped)
    skill_rows = []
    for r in extract_insert_rows(ref / "playercreateinfo_skills.sql", "playercreateinfo_skills"):
        mapped = map_skill_row(r)
        if mapped is not None:
            skill_rows.append(mapped)

    args.tables_out.parent.mkdir(parents=True, exist_ok=True)
    args.tables_out.write_text(TABLES_SQL, encoding="utf-8")

    lines: list[str] = [
        "-- Starter spawn / spells / skills from firelands-cata-ref (Trinity 4.3.4).",
        "USE `firelands_world`;",
        "",
        DATA_SCHEMA_PREAMBLE.strip(),
        "",
        "DELETE FROM `playercreateinfo`;",
        "",
    ]
    append_batched(
        lines,
        "REPLACE INTO `playercreateinfo` "
        "(`race`,`class`,`map`,`zone`,`position_x`,`position_y`,`position_z`,`orientation`) VALUES",
        [f"({r})" for r in spawn_rows],
        batch_size=80,
    )
    append_batched(
        lines,
        "REPLACE INTO `playercreateinfo_spell` (`raceMask`,`classMask`,`spellId`) VALUES",
        [f"({r})" for r in spell_rows],
        batch_size=120,
    )
    append_batched(
        lines,
        "REPLACE INTO `playercreateinfo_skill` (`raceMask`,`classMask`,`skillId`,`rank`) VALUES",
        [f"({r})" for r in skill_rows],
        batch_size=120,
    )
    args.data_out.write_text("\n".join(lines), encoding="utf-8")
    print(f"Wrote {args.tables_out} and {args.data_out}")
    print(f"  playercreateinfo: {len(spawn_rows)} rows")
    print(f"  playercreateinfo_spell: {len(spell_rows)} rows")
    print(f"  playercreateinfo_skill: {len(skill_rows)} rows")


if __name__ == "__main__":
    main()
